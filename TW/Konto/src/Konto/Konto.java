package Konto;

public class Konto {
	
	private long ktoNummer;
	private int ktoStand;

	public Konto(long ktoNummer, int ktoStand){
		
		this.ktoNummer = ktoNummer;
		this.ktoStand = ktoStand;
		
	}
	
	public int getKtoStand(){
		
		return this.ktoStand;
		
	}
	
	public void einzahlen(int betrag){
		
		this.ktoStand = this.ktoStand + betrag;
		
	}
	
	public void auszahlen(int betrag){
		
		this.ktoStand = this.ktoStand - betrag;
		
	}
}
